package com.example.demo.ui.courses.add;

public class CourseAddProfile {
    private String name;
    private String time;

    public CourseAddProfile(String name, String time) {
        this.name = name;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public String getTime() {
        return time;
    }
}
